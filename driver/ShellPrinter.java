package driver;

/**
 * ShellPrinter takes a string and then prints it to the shell.
 * 
 * @author Val Wikblad
 *
 */
public class ShellPrinter {
  
  /**
   * print takes a string and then prints it directly to the shell.
   * 
   * @param output      The formatted string to be printed to the shell.
   */
  public void print(String output) {
    System.out.println(output);
  }
}